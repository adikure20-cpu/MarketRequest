// --- i18n Utility ---
const I18n = {
    currentLang: 'de',

    init(storageKey, defaultLang) {
        this.storageKey = storageKey || 'appLang';
        this.currentLang = localStorage.getItem(this.storageKey) || defaultLang || 'de';
        return this.currentLang;
    },

    setLang(lang) {
        if (!TRANSLATIONS[lang]) return;
        this.currentLang = lang;
        localStorage.setItem(this.storageKey, lang);
    },

    t(key) {
        const translations = TRANSLATIONS[this.currentLang] || TRANSLATIONS.de;
        return translations[key] || TRANSLATIONS.de[key] || key;
    },

    getStatusLabel(status) {
        return this.t('status_label_' + status) || status;
    },

    getStatusMessage(status) {
        return this.t('status_' + status) || status;
    },

    getTimeAgo(dateStr) {
        const now = new Date();
        const date = new Date(dateStr);
        const diff = Math.floor((now - date) / 1000);

        if (diff < 60) return this.t('time_just_now');
        if (diff < 3600) return Math.floor(diff / 60) + ' ' + this.t('time_min_ago');
        if (diff < 86400) return Math.floor(diff / 3600) + ' ' + this.t('time_hours_ago');
        return Math.floor(diff / 86400) + ' ' + this.t('time_days_ago');
    }
};
